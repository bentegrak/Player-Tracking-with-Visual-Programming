    private void jMenuItem1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem1ActionPerformed
        JPasswordField passwordField = new JPasswordField();
        JFormattedTextField usernameField = new JFormattedTextField();

        Object[] message = {
                "Kullanıcı Adı:", usernameField,
                "Şifre:", passwordField
        };

        int option = JOptionPane.showOptionDialog(null, message, "Kullanıcı Doğrulama",
                JOptionPane.OK_CANCEL_OPTION, JOptionPane.PLAIN_MESSAGE, null, null, null);

        if (option == JOptionPane.OK_OPTION) {
            String kullaniciAdi = usernameField.getText();
            String sifre = new String(passwordField.getPassword());

            if (kullaniciAdi.equals(dogruKullaniciAdi) && sifre.equals(dogruSifre)) {
                // Kadroyu kaydetme işlemi
                JFileChooser fileChooser = new JFileChooser();
                int returnValue = fileChooser.showSaveDialog(null);

                if (returnValue == JFileChooser.APPROVE_OPTION) {
                    File selectedFile = fileChooser.getSelectedFile();
                    try {
                        FileWriter writer = new FileWriter(selectedFile);
                        DefaultListModel<String> model = (DefaultListModel<String>) jList1.getModel();
                        for (int i = 0; i < model.getSize(); i++) {
                            writer.write(model.getElementAt(i) + "\n");
                        }
                        writer.close();
                        JOptionPane.showMessageDialog(null, "Kadro başarıyla kaydedildi.", "Bilgi", JOptionPane.INFORMATION_MESSAGE);
                    } catch (IOException ex) {
                        JOptionPane.showMessageDialog(null, "Kaydetme işlemi sırasında bir hata oluştu.", "Hata", JOptionPane.ERROR_MESSAGE);
                    }
                }
            } else {
                JOptionPane.showMessageDialog(null, "Geçersiz kullanıcı adı veya şifre!", "Hata", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jMenuItem1ActionPerformed

