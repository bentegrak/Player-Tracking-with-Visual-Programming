    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        JFileChooser fileChooser = new JFileChooser();

        int result = fileChooser.showOpenDialog(this);

        if (result == JFileChooser.APPROVE_OPTION) {
            File file = fileChooser.getSelectedFile();

            try (BufferedReader reader = new BufferedReader(new FileReader(file))) {
                String line;
                while ((line = reader.readLine()) != null) {
                    String[] playerData = line.split(",");

                    if (playerData.length >= 3) {
                        String playerName = playerData[0].trim();
                        String playerPosition = playerData[1].trim();
                        String playerAge = playerData[2].trim();

                        ekleOyuncuTablosuna(playerName, playerPosition, playerAge);
                    }
                }
                JOptionPane.showMessageDialog(this, "Oyuncular başarıyla içe aktarıldı!", "Bilgi", JOptionPane.INFORMATION_MESSAGE);
            } catch (IOException e) {
                JOptionPane.showMessageDialog(this, "Dosya okuma hatası: " + e.getMessage(), "Hata", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

