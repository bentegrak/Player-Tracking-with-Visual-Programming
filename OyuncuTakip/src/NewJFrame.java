    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
    int selectedRow = jTable1.getSelectedRow();
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Lütfen bir oyuncu seçin!", "Uyarı", JOptionPane.WARNING_MESSAGE);
        return;
    }

    String oyuncuIsim = jTable1.getValueAt(selectedRow, 0).toString();
    String oyuncuMevki = jTable1.getValueAt(selectedRow, 1).toString();

    // Kontrol etmek için jList'in içindeki verileri döngü ile kontrol edin
    for (int i = 0; i < model.size(); i++) {
        String listItem = model.getElementAt(i);
        if (listItem.equals(oyuncuIsim + " - " + oyuncuMevki)) {
            JOptionPane.showMessageDialog(this, "Bu oyuncu zaten listede bulunuyor!", "Uyarı", JOptionPane.WARNING_MESSAGE);
            return;
        }
    }

    if (model.size() >= 11) {
        JOptionPane.showMessageDialog(this, "Kadroda 11 oyuncu bulunuyor!", "Uyarı", JOptionPane.WARNING_MESSAGE);
        return;
    }

    model.addElement(oyuncuIsim + " - " + oyuncuMevki); // Veriyi DefaultListModel'e ekleyin
    jList1.setModel(model); // DefaultListModel'i jList1'e ayarlayın

    JOptionPane.showMessageDialog(this, "Oyuncu başarıyla eklendi!", "Bilgi", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_jButton4ActionPerformed

